#calculator

num_1 = int(input("Enter the first number: "))
num_2 = int(input("Enter the second number: "))
op = input("Enter the operator: ")

#if_statement

if op == "+":
    print(str("The answer is") (num_1 + num_2))
if op == "-":
    print(str("The answer is") (num_1 - num_2))
if op == "/":
    print(str("The answer is") (num_1 / num_2))
if op == "*":
    print(str("The answer is") (num_1 * num_2))

